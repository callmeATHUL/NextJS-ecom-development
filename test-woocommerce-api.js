// Helper function to make authenticated WooCommerce requests
async function wooCommerceRequest(endpoint, params = {}) {
  try {
    const url = `${WOOCOMMERCE_API_BASE}${endpoint}`;
    const response = await axios.get(url, {
      params: {
        consumer_key: CONSUMER_KEY,
        consumer_secret: CONSUMER_SECRET,
        ...params
      },
      timeout: 15000,
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json',
      }
    });
    return { success: true, data: response.data, status: response.status };
  } catch (error) {
    return { 
      success: false, 
      error: error.message,
      status: error.response?.status,
      data: error.response?.data
    };
  }
}

// Helper function to make CoCart requests
async function coCartRequest(endpoint, method = 'GET', data = null) {
  try {
    const url = `${COCART_API_BASE}${endpoint}`;
    const config = {
      method,
      url,
      timeout: 10000,
      headers: {
        'Content-Type': 'application/json',
      }
    };
    
    if (data) {
      config.data = data;
    }
    
    const response = await axios(config);
    return { success: true, data: response.data, status: response.status };
  } catch (error) {
    return { 
      success: false, 
      error: error.message,
      status: error.response?.status,
      data: error.response?.data
    };
  }
}

// Test logging function
function logTest(testName, result, details = '') {
  const status = result.success ? '‚úÖ PASS' : '‚ùå FAIL';
  console.log(`${status} ${testName}`);
  
  if (result.success) {
    testResults.summary.passed++;
    if (details) console.log(`   ‚ÑπÔ∏è  ${details}`);
  } else {
    testResults.summary.failed++;
    console.log(`   ‚ùå Error: ${result.error}`);
    if (result.status) console.log(`   üìã Status: ${result.status}`);
    if (result.data) console.log(`   üìÑ Data:`, result.data);
  }
  
  testResults.summary.total++;
  console.log(''); // Empty line for readability
}

